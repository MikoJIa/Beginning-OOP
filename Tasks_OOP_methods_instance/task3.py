import math

class Point:
    def set_coordinates(self, x, y):
        self.x = x
        self.y = y


    def get_distance(self, p2):
        if isinstance(p2, Point):
            return math.sqrt((self.x - p2.x)**2 + (self.y - p2.y)**2)
        else:
            print('–ü–µ—Ä–µ–¥–∞–Ω–∞ –Ω–µ —Ç–æ—á–∫–∞')

